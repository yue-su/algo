import heapq

class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next

class Solution:
    def mergeKLists(self, lists: List[Optional[ListNode]]) -> Optional[ListNode]:
        if not lists:
            return None

        pq = []
        dummy = ListNode(-1)
        p = dummy
        counter = 0

        for head in lists:
            if head:
                heapq.heappush(pq, (head.val, counter, head))
                counter += 1

        while pq:
            node = heapq.heappop(pq)[2]
            p.next = node
            if node.next:
                heapq.heappush(pq, (node.next.val, counter, node.next))
            p = p.next
            counter += 1

        return dummy.next


